#!/usr/bin/env bash
# Submit Phase 8 P0 Milestone 7 baseline evaluation with durable env propagation.
#
# Usage examples:
#   scripts/calculco/submit_phase8_p0_evaluate_baselines.sh development
#   scripts/calculco/submit_phase8_p0_evaluate_baselines.sh final
#   MILESTONE7_BASELINES=linear,xgboost \
#     scripts/calculco/submit_phase8_p0_evaluate_baselines.sh development
#
# The selected env is written to scripts/calculco/phase8_p0_milestone7_job.env
# (gitignored) and sourced inside phase8_p0_evaluate_baselines.sh by the OAR worker.

set -euo pipefail

script_dir="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
: "${PROJECT_HOME:=$HOME/ResearchModule}"

mode="${1:-development}"
if [[ "$mode" != "development" && "$mode" != "final" ]]; then
  echo "Usage: $0 [development|final]" >&2
  exit 2
fi

frozen_selection=0
if [[ "$mode" == "final" ]]; then
  frozen_selection=1
  if [[ ! -f "$PROJECT_HOME/reports/phase8_p0_milestone7_frozen_selection.json" ]]; then
    echo "ERROR: missing reports/phase8_p0_milestone7_frozen_selection.json" >&2
    echo "Run development evaluation and freeze validation choices first." >&2
    exit 1
  fi
fi

: "${MILESTONE7_TOP_K:=1,3,5,10}"
: "${MILESTONE7_BASELINES:=random,global_popularity,condition_popularity,linear,xgboost}"
: "${MILESTONE7_CONDITION_TOKENS:=}"

env_file="$script_dir/phase8_p0_milestone7_job.env"
cat >"$env_file" <<EOF
# Generated by submit_phase8_p0_evaluate_baselines.sh on $(date -u '+%Y-%m-%dT%H:%M:%SZ')
MILESTONE7_MODE=$mode
MILESTONE7_TOP_K=$MILESTONE7_TOP_K
MILESTONE7_FROZEN_SELECTION=$frozen_selection
MILESTONE7_BASELINES=$MILESTONE7_BASELINES
MILESTONE7_CONDITION_TOKENS=$MILESTONE7_CONDITION_TOKENS
EOF

mkdir -p "$script_dir/logs"
echo "Wrote $env_file"
cat "$env_file"

oarsub -O "$PROJECT_HOME/scripts/calculco/logs/rm_phase8_p0_eval_%jobid%.out" \
       -E "$PROJECT_HOME/scripts/calculco/logs/rm_phase8_p0_eval_%jobid%.err" \
       -S "$PROJECT_HOME/scripts/calculco/phase8_p0_evaluate_baselines.sh"
